% Объявляем предикаты динамическими для изменения базы данных во время работы
:- dynamic supplier/6.
:- dynamic material/10.
:- dynamic supply/7.

% База данных по умолчанию

% Поставщики: supplier(ID, Название, ФИО_контакта, РасчСчет, Телефон, Факс).
supplier(1, 'ООО МеталлПром', 'Иванов И.И.', '4070281000000', '8-495-111', '8-495-112').
supplier(2, 'ООО Пластик', 'Петров П.П.', '4070282000000', '8-495-333', '8-495-334').

% Сырье: material(ID, Тип, Марка, МинЗапас, Задержка, Цена, Продукты, ОбъемНеобх, ОбъемРеал, ОбъемНаЕд).
material(101, 'Сталь', 'Ст3', 1000, '2 дня', 50000, 'Станки', 500, 550, 5).
material(102, 'Пластик', 'ПВХ', 500, '1 день', 20000, 'Корпуса', 200, 200, 2).
material(103, 'Алюминий', 'АД31', 300, '3 дня', 80000, 'Детали', 150, 150, 1).

% Поставки (Связь N:M): supply(ID_поставки, ID_поставщика, ID_сырья, ДатаДоговор, ДатаРеал, Метод, Стоимость).
supply(1001, 1, 101, '01.03.2026', '02.03.2026', 'Авто', 5000).
supply(1002, 1, 103, '05.03.2026', '05.03.2026', 'ЖД', 15000).
supply(1003, 2, 102, '10.03.2026', '12.03.2026', 'Поезд', 3000).

main :-
    repeat,
    nl, write('БД: Отдел поставок'), nl,
    write('1 - Добавить нового поставщика'), nl,
    write('2 - Показать всех поставщиков'), nl,
    write('3 - Запрос: показать все поставки конкретного поставщика (Связь N:M)'), nl,
    write('4 - Удалить поставщика и историю его поставок'), nl,
    write('0 - Выход из программы'), nl,
    write('Ваш выбор (цифра и точка в конце): '), 
    read(Choice),
    menu_do(Choice),
    Choice == 0, !. % Выход из цикла repeat, если ввели 0

% 1. Добавление поставщика
menu_do(1) :-
    write('Введите ID поставщика (число): '), read(ID),
    write('Введите название (в одинарных кавычках): '), read(Name),
    write('Введите ФИО контакта (в кавычках): '), read(Contact),
    write('Введите расч. счет (в кавычках): '), read(Account),
    write('Введите телефон (в кавычках): '), read(Phone),
    write('Введите факс (в кавычках): '), read(Fax),
    assertz(supplier(ID, Name, Contact, Account, Phone, Fax)),
    write('Поставщик успешно добавлен!'), nl, !.

% 2. Вывод списка поставщиков
menu_do(2) :-
    write('Список поставщиков'), nl,
    supplier(ID, Name, Contact, Account, Phone, Fax),
    write('ID: '), write(ID), 
    write(' | Компании: '), write(Name), 
    write(' | Контакт: '), write(Contact), 
    write(' | Р/С: '), write(Account), 
    write(' | Тел: '), write(Phone), 
    write(' | Факс: '), write(Fax), nl,
    fail. 
menu_do(2) :- !. 

% 3. Вывод поставок конкретного поставщика (Демонстрация связи N:M)
menu_do(3) :-
    write('Введите ID поставщика для просмотра поставок: '), read(SuppID),
    supplier(SuppID, Name, _, _, _, _), 
    write('Поставки от компании: '), write(Name), write(' ---'), nl,
    % Ищем все поставки этого поставщика
    supply(SupplyID, SuppID, MatID, DateC, DateR, Method, Cost),
    % По ID сырья подтягиваем данные о самом сырье из таблицы material
    material(MatID, Type, Brand, _, _, _, _, _, _, _), 
    write('Договор №'), write(SupplyID), 
    write(' | Сырье: '), write(Type), write(' ('), write(Brand), write(')'),
    write(' | План: '), write(DateC), write(' | Факт: '), write(DateR), 
    write(' | Доставка: '), write(Method), write(' | Цена дост.: '), write(Cost), nl,
    fail.
menu_do(3) :- !. 

% 4. Удаление поставщика (и его поставок)
menu_do(4) :-
    write('Введите ID поставщика для удаления: '), read(ID),
    % Удаляем саму компанию
    retractall(supplier(ID, _, _, _, _, _)), 
    % Удаляем все записи о ее поставках
    retractall(supply(_, ID, _, _, _, _, _)), 
    write('>>> Поставщик и все связанные с ним поставки были удалены!'), nl, !.

% 0. Выход
menu_do(0) :-
    write('Работа с базой данных завершена.'), nl, !.

% Обработка неправильного ввода
menu_do(_) :-
    write('>>> Ошибка: Неверный пункт меню! Попробуйте снова.'), nl, !.
