% Задание 4: Мэри, Бет и фрукты

% Разрешаем предикату любит/2 быть определенным в разных местах файла
:- discontiguous любит/2.

% ----- Факты о том, что любит Мэри -----
любит(мэри, персики).
любит(мэри, кукуруза).
любит(мэри, яблоки).

% ----- Факты о фруктах -----
фрукт(персики).
фрукт(яблоки).
фрукт(апельсины).

% ----- Факты о цвете -----
цвет(персики, желтый).
цвет(апельсины, оранжевый).
цвет(яблоки, красный).
цвет(яблоки, желтый).      % Яблоки могут быть и красными, и желтыми

% ----- Правила для Бет -----

% Правило 1: Бет любит красные фрукты, которые любит Мэри
любит(бет, Что) :-
    любит(мэри, Что),      % Мэри это любит
    фрукт(Что),            % Это фрукт
    цвет(Что, красный).    % И он красного цвета

% Правило 2: Бет любит кукурузу (даже если это не фрукт)
любит(бет, кукуруза) :-
    любит(мэри, кукуруза).  % Мэри любит кукурузу
